                                <h1 class="page-title">Income Tax &amp; VAT</h1>
                                
                                <div class="news-body">
                                
                                    <p class="call-out">
                                    Income tax and <a href="vat.html">VAT</a> are an integral part of a business. Legitimate tax planning and good systems can ensure that the correct amount of taxes is paid. However, should mistakes be made, various penalties and use of money interest can be imposed?
                                    </p>
                                
                                    

                                    <p>
                                       To assist you with your income tax and <a href="vat.html">VAT</a> obligations we provide a brief and high level summary of the income tax and <a href="vat.html">VAT</a> rules that may apply to you. Our summary is of a general nature and should only be used as guide.

                                        
                                    </p>

                                    <h4 style="margin-top:40px; margin-bottom:15px;">What Costs Can I Claim for Income Tax purposes?</h4>
                                    <h5 style="margin-top:10px; margin-bottom:10px;">Introduction</h5>

                                    <p>
                                        As a starting point, any costs that have been incurred in your business to generate income are generally tax deductible. However, there are specific tax rules for costs such as the purchase of assets, and other capital costs. These costs generally have an ongoing benefit to the business over a period of years. Therefore, you may be able to claim a proportion of their cost each year, as a depreciation expense.
                                    </p>
                                    <p>
                                        If you have "incurred" an expense, you can claim this cost even though you may not yet have paid it.
                                    </p>

                                    <p>
                                        Some payments appear to be necessary for business but could be of a private or capital nature, such as:

                                        <ul>
                                            <li>Traveling from home to work and back (unless in some cases if you have an office at home).</li>
                                            <li>The clothes you wear to work unless they are protective clothing like overalls for a painter and sneakers for a roofer. To qualify, the clothes have to be subject to undue wear and tear or of a specialised type - not normal apparel. For example, work-boots, overalls, spectacles.</li>
                                            <li>Costs of commencing a business, such as the costs of creating a limited liability company.</li>
                                            <li>The cost of taking your partner on an overseas business trip unless you are both travelling on business related matters.</li>
                                            <li>The costs relating to installing new machinery, including travel to go and evaluate it.</li>
                                            <li>The debt repayment portion of hire purchase, lease or other loans.</li>
                                        </ul>
                                    </p>

                                    <p style="margin-top:15px;">
                                        If you are unsure as to whether you can claim an expense please give us a <a href="contact_us.html">call</a>.
                                    </p>

                                    <h5 style="margin-top:20px; margin-bottom:10px;">Home Office Claim</h5>
                                    <p>
                                        If you are conducting a business from your Owned, then you will be entitled to claim a portion of the following expenses for income tax purposes:

                                        <ul>
                                            <li>Rates</li>
                                            <li>Insurance</li>
                                            <li>Interest on Mortgage (no Rent claim)</li>
                                            <li>Repairs and Maintenance</li>
                                            <li>Power</li>
                                        </ul>
                                    </p>

                                    <p>
                                        The amount of these deductions is normally based upon the percentage of the home that is used for business purposes, which is generally calculated on a square footage basis.
                                    </p>
                                    <p>
                                        It is MTLCâ€™s policy not to claim depreciation for Owned office purposes as the eventual sale of the home will likely trigger a depreciation recovery issue, which will be subject to income tax. However, if depreciation has been calculated in the past then you must continue to do so.
                                    </p>
                                    <p>
                                        If you are unsure of whether you are conducting a business at home or you wish to establish your home as a work place, the following issues should be considered:

                                        <ul>
                                            <li>The proportion of the home/garage set aside for business</li>
                                            <li>The reasons for work being undertaken at, or from home</li>
                                            <li>The degree of income producing activity occurring at, or from the home</li>
                                            <li>Storage of business equipment at the home</li>
                                            <li>Whether work from home is integral to the business</li>
                                        </ul>
                                    </p>

                                    <p>
                                        Where you only earn passive income such as interest and dividends, it is highly unlikely that a home office claim can be made.
                                    </p>

